public class Nodo {

    String token;
    String descripcion;
    
    public Nodo() {
        
    }

    public void setToken(String token) {
        this.token = token;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    
    public String getToken() {
        return token;
    }

    public String getDescripcion() {
        return descripcion;
    }
    
    
}
